CREATE TABLE `User` (
	`UserPK`	VARCHAR(30)	NOT NULL,
	`UserEmail`	VARCHAR(30)	NOT NULL,
	`UserPassword`	VARCHAR(30)	NOT NULL,
	`UserCreateDate`	DATETIME	NOT NULL	DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE `Excercise` (
	`ExercisePK`	VARCHAR(30)	NOT NULL,
	`Title`	VARCHAR(30)	NOT NULL,
	`Contents`	TEXT	NOT NULL,
	`DevEnv`	VARCHAR(30)	NOT NULL,
	`GitUrl`	VARCHAR(30)	NOT NULL,
	`PropertyFile`	TEXT	NOT NULL
);

CREATE TABLE `ExcerciseResult` (
	`ExerciseResultPK`	VARCHAR(30)	NOT NULL,
	`ExercisePK`	VARCHAR(30)	NOT NULL,
	`UserPK`	VARCHAR(30)	NOT NULL,
	`Score`	INT	NOT NULL,
	`GitUrl`	VARCHAR(30)	NULL,
	`CreateDate`	DATETIME	NOT NULL	DEFAULT CURRENT_TIMESTAMP,
	`EndDate`	DATETIME	NULL
);

